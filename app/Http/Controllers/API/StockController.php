     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StockRequest $request, Produit $produit)
    {
        $nbreAjout = $request->nombre;
        $produit->stock = $produit->stock + $nbreAjout;
        $produit->save();
        return response()->json([
            'message' => "Stock du produit $produit->nom modifié avec succès",
            'produit' => $produit,
        ]);
    }

